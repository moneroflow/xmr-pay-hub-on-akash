/**
 * Get translation for a given language code. Falls back to English.
 * Supports auto-detection from browser language.
 */
export function getCheckoutTranslation(langCode?: string): CheckoutTranslation {
  if (!langCode || langCode === 'auto') {
    // Auto-detect from browser
    const browserLang = navigator.language?.split('-')[0] || 'en';
    return translations[browserLang] || translations.en;
  }
  return translations[langCode] || translations.en;
}

export function getSupportedLanguages(): { code: string; name: string }[] {
  return Object.keys(translations).map(code => ({
    code,
    name: new Intl.DisplayNames([code], { type: 'language' }).of(code) || code,
  }));
}
